
'''
基本运算符
+ - × /
'''

# +
first_number = 10
second_number = 5
result = first_number + second_number
print(result)

# -
result = second_number - first_number
print(result)

# *
result = first_number * second_number
print(result)

# /
result = first_number / second_number
print(result)
print(type(result))

